    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String manv = this.tfManv.getText();
        String ten = this.tfTen.getText();
        String sdt = this.tfSDT.getText();
        String dc = this.tfDiachi.getText();
        String cm = this.tfCMND.getText();
        String que = this.tfQue.getText();
        String email = this.tfEmail.getText();
        String macv = this.cbMacv.getName();
        String gc = this.tfGhichu.getText();
        int tt = this.cbTrangthai.getItemCount();

        if (manv.isEmpty()) {
            JOptionPane.showMessageDialog(null, "ma nv trong");
        } else if (ten.isEmpty()) {
            JOptionPane.showMessageDialog(null, "ten nv trong");
        } else if (sdt.isEmpty()) {
            JOptionPane.showMessageDialog(null, "so dien thoai trong");
        } else if (dc.isEmpty()) {
            JOptionPane.showMessageDialog(null, "dia chi trong");
        } else if (cm.isEmpty()) {
            JOptionPane.showMessageDialog(null, "cmnd trong");
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(null, "email trong");
        } else if (macv.isEmpty()) {
            JOptionPane.showMessageDialog(null, "ma cv trong");
        } else {
//              JOptionPane.showMessageDialog(null, "ok");
            Nhan_Vien nv=new Nhan_Vien(
            manv,
            ten,
            sdt,
            dc,
            cm,
            que,
            email,
            macv,
            this.ccNgaysinh.getDate(),
            this.ccNgayBD.getDate(),
            gc,
            tt);
            try {
                if (new NhanVienImp().add(nv)) {
                    JOptionPane.showMessageDialog(null, "luu thanh cong");
                } else {
                    JOptionPane.showMessageDialog(null, "luu that bai");
                }

            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnLuuActionPerformed
   
